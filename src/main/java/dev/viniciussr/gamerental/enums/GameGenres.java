package dev.viniciussr.gamerental.enums;

/**
 * Representa os gêneros de jogos disponíveis para classificação no sistema.
 * <p>
 * Utilizado para categorizar os jogos com base em seu estilo, mecânicas
 * e ambientação.
 * </p>
 */
public enum GameGenres {

    /**
     * Jogos de ação em geral.
     */
    ACTION,

    /**
     * Jogos de aventura.
     */
    ADVENTURE,

    /**
     * Jogos no estilo battle royale.
     */
    BATTLE_ROYALE,

    /**
     * Jogos de luta.
     */
    FIGHTING,

    /**
     * Jogos de tiro em primeira pessoa (First-Person Shooter).
     */
    FPS,

    /**
     * Jogos no estilo hack and slash.
     */
    HACK_N_SLASH,

    /**
     * Jogos de terror.
     */
    HORROR,

    /**
     * Jogos no estilo metroidvania.
     */
    METROIDVANIA,

    /**
     * Multiplayer Online Battle Arena.
     */
    MOBA,

    /**
     * Massively Multiplayer Online Role-Playing Game.
     */
    MMORPG,

    /**
     * Jogos de plataforma.
     */
    PLATFORMER,

    /**
     * Jogos de quebra-cabeça.
     */
    PUZZLE,

    /**
     * Jogos de corrida.
     */
    RACING,

    /**
     * Jogos no estilo roguelike.
     */
    ROGUELIKE,

    /**
     * Role-Playing Game.
     */
    RPG,

    /**
     * Real-Time Strategy (Estratégia em tempo real).
     */
    RTS,

    /**
     * Jogos de simulação.
     */
    SIMULATION,

    /**
     * Jogos no estilo soulslike.
     */
    SOULSLIKE,

    /**
     * Jogos de esportes.
     */
    SPORTS,

    /**
     * Jogos de sobrevivência.
     */
    SURVIVAL
}